input = input("How are you feeling today? ").lower()

if "happy" in input:
    print("I'm glad to hear that!")
elif "sad" in input:
    print("I'm sorry you're feeling this way.")
elif "angry" in input:
    print("It's okay to feel angry sometimes.")
else:
    print("Thanks for sharing your feelings!")
# This code checks the user's input for specific emotions (happy, sad, angry)
# and responds with an appropriate message.
# It demonstrates basic string handling and conditional statements in Python.